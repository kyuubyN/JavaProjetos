public class Variaveis {
    public static void main(String[] arg) throws Exception{
        /*Integer number*/
        int idade;
        idade = 18;
        System.out.println(idade);
        /*Float number*/
        float pi;
        pi = 3.14F;
        System.out.println(pi);
        /*Bool True or False*/
        boolean vdd;
        vdd = true;
        System.out.println(vdd);
        /*char*/
        Character letra = 'a';
        System.out.println(letra);
        /*String*/
        String nome = "Kaue";
        System.out.println(nome);
        /*Variavel final*/
        final int NUMERO_FINAL = 10;
        /*NUMERO_FINAL = 5; Isso por exemplo, não funciona por ter aquele final no começo da variavel*/
        System.out.println(NUMERO_FINAL);
    }
}